    private void cargarUsername() {
        try {
            BufferedReader br = new BufferedReader(new FileReader("Usuarios.txt"));
            String line = br.readLine();
            if (line != null) {
                String[] parts = line.split(";");
                if (parts.length >= 3) {
                    String username;
                    if (parts.length == 5) {
                        // Si es un arreglo, el nombre de usuario est치 en la segunda posici칩n
                        username = parts[1];
                    } else {
                        // Si no es un arreglo, el nombre de usuario est치 en la tercera posici칩n
                        username = parts[2];
                    }
                    setWelcomeMessage("Bienvenido, " + username + "!");
                }
            }
            br.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
